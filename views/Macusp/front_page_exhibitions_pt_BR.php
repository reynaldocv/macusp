<h4>
Nesta área, é possível consultar as informações sobre as exposições promovidas pelo MAC USP e também o histórico de participação das
obras da Coleção em exposições realizadas por outras instituições.
</h4>

<h4>As exposições estão organizadas em duas categorias:</h4>

    <ul>
    <li style="margin:-8px"> <h4> <b> Exposições do MAC USP: </b> realizadas pelo MAC USP, em qualquer uma de suas sedes ou em espaços parceiros. Nesses casos,
são listadas todas as obras participantes, tanto aquelas que fazem parte da Coleção MAC USP, quanto outros trabalhos. </h4></li>
    <li style="margin:-8px"> <h4> <b> Exposições de outras instituições: </b> realizadas por outras organizações, com a participação de uma ou mais obras do acervo do
MAC USP. Nesses casos, são listadas apenas as obras da Coleção MAC USP, e não a totalidade de obras presentes na exibição. A
data da exposição pode ser anterior ou posterior à incorporação da obra ao acervo do Museu.</h4></li>
</ul>


<h4>As informações sobre as exposições estão dispostas da seguinte maneira:</h4>



<ul style="list-style-type: none; padding:-20px">
    <li style="margin:-8px"> <h4> <b> Título:               </b> nome da exposição. </h4> </li>
    <li style="margin:-8px"> <h4> <b> Local da exposição:   </b> Instituição e cidade onde a exposição foi realizada. </h4> </li>
    <li style="margin:-8px"> <h4> <b> Data de início: </b> dia/mês/ano  | <b>Data de término:</b> dia/mês/ano. </h4> </li>
    <li style="margin:-8px"> <h4> <b> Sinopse: </b> Breve descrição da exposição. </h4> </li>
    <li style="margin:-8px"> <h4> <b> Instituição: </b> Organização responsável pela realização da exposição.</h4> </li>
    <li style="margin:-8px"> <h4> <b> Responsável: </b>  Indivíduos responsáveis pela curadoria e/ou organização da exposição. </h4> </li>
    <li style="margin:-8px"> <h4> <b> Obras do MAC USP: </b> Obras do acervo MAC USP apresentadas na exposição. </h4> </li>
    <li style="margin:-8px"> <h4> <b> Outras obras: </b> Obras não pertencentes ao acervo do MAC USP, mas que também participaram da exposição (informação disponível somente para as Exposições do MAC USP).</h4> </li>
    
 
</ul>



<h4>Você pode encontrar duas ou mais exposições com o mesmo título, mas com data e local diferentes. Isso significa que se trata de uma
exposição itinerante, apresentada em diferentes locais.</h4>
<h4>As informações aqui disponíveis são constantemente revisadas e complementadas pela equipe do Museu. Caso deseje ampliar sua
pesquisa, entre em contato pelo e-mail: <b>docmac@usp.br</b>.</h4>

