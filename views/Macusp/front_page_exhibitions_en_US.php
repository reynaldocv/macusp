<h4>
In this area, you can find information about exhibitions promoted by MAC USP and also the history of participation of works from the Collection in exhibitions held by other institutions.

</h4>

<h4>The exhibitions are organized into two categories:</h4>

<ul>

<li style="margin:-8px"> <h4> <b> MAC USP Exhibitions: </b> held by MAC USP, in any of its locations or in partner spaces. In these cases, all participating works are listed, both those that are part of the MAC USP Collection and other works. </h4></li>

<li style="margin:-8px"> <h4> <b> Exhibitions from other institutions: </b> held by other organizations, with the participation of one or more works from the MAC USP collection. In these cases, only the works from the MAC USP Collection are listed, and not all the works present in the exhibition. The exhibition date may be before or after the work's incorporation into the Museum's collection.

</ul>

<h4> The exhibition information is arranged as follows: </h4>

<ul style="list-style-type: none; padding:-20px">
<li style="margin:-8px"> <h4> <b> Title: </b> Name of the exhibition. </h4> </li>
<li style="margin:-8px"> <h4> <b> Location of the exhibition: </b> Institution and city where the exhibition was held. </h4> </li>
<li style="margin:-8px"> <h4> <b> Start date: </b> day/month/year | <b> End date: </b> day/month/year. </h4> </li>
<li style="margin:-8px"> <h4> <b> Synopsis: </b> Brief description of the exhibition. </h4></li>
<li style="margin:-8px"> <h4> <b>Institution:</b> Organization responsible for holding the exhibition.</h4></li>
<li style="margin:-8px"> <h4> <b>Responsible:</b> Individuals responsible for curating and/or organizing the exhibition.</h4></li>
<li style="margin:-8px"> <h4> <b>Works from the MAC USP:</b> Works from the MAC USP collection presented in the exhibition.</h4></li>
<li style="margin:-8px"> <h4> <b>Other works:</b> Works not belonging to the MAC USP collection, but which also participated in the exhibition (information available only for MAC USP Exhibitions).</h4></li>

</ul>

<h4>You may find two or more exhibitions with the same title, but with different dates and locations. This means that it is a
traveling exhibition, presented in different locations.</h4>
<h4>The information available here is constantly reviewed and supplemented by the Museum team. If you wish to expand your
research, please contact us by email: <b>docmac@usp.br</b>.</h4>